<h3>Overview</h3>
<p>
	No-CMS is a CMS-framework. It is a <a href="help/topic/no_cms_as_cms">CMS</a>
	and a <a href="help/topic/no_cms_as_framework">framework</a> in the
	same time. No-CMS is a basic CMS with some default features such as
	user authorization, menu, module and theme management. It is fully
	customizable and extensible, you can make your own module and your own
	themes. It provide freedom to make your very own CMS, which is not
	provided very well by any other CMS.
</p>
