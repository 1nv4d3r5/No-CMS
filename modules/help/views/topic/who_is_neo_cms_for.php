<h3>Who Is Neo CMS For?</h3>
Neo CMS will be good for you if you say yes for majority of these statement:
<ul>
    <li> You are a web developer who use CodeIgniter framework.</li>
    <li> You are tired of building the same things such an authorization-authentication for every project.</li>
    <li> You find that some part of your old project can be used for your next project.</li>
    <li> You are happy with CodeIgniter but you think some plug-ins and features should be provided by default.</li>
    <li> You want a simple and easy to learn framework that has 100% compatibility with CodeIgniter.</li>
    <li> You don't want to learn too many new terms.</li>
    <li> You are familiar with HMVC plugins, and you think it is one of "should be exists" feature in CodeIgniter.</li>
    <li> You are in tight deadline, at least you need to provide the prototype to your client.</li>
</ul>