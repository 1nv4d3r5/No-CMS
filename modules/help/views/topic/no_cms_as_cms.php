<h3>No-CMS as CMS</h3>
<p>No-CMS is a good enough CMS. It is different from Wordpress, Drupal
	or Joomla. Those CMS are built from developers for users. No-CMS is
	built by developer for developers, although everyone else can still use
	it as well. The main purpose of this CMS is to provide a good start of
	web application project, especially for CodeIgniter developer.</p>
